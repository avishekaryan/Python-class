# WAP to input length and breadth and calculate area of rectangle.
# Formula : Area = length * breadth

l = float(input("Enter given length = "))
b = float(input("Enter given breadth = "))

print(f"Area of Rectangle = length * breadth = {l} * {b} = " , l*b)



# Also, Perimeter

print(f"Perimeter of Rectangle = 2(l+b) = 2({l}+{b}) = ", 2*(l+b))